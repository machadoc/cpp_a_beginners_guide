// using an operatot

#include <iostream>
using namespace std;

int main() {
  int length;  // this declares a variable
  int with;    // this declares another variable
  int area;    // this does too

  length = 7;  // this assigns 7 to length
  with = 5;    // this assigns 5 to with

  area = length * with;  // compute area

  cout << " the area is ";
  cout << area;  // this displays 35

  return 0;
}